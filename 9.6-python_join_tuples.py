#Join Two Tuples

"""
Iki veya daha fazla tuple'i birlestirmek icin '+' operatorunu kullanabiliriz.

Iki tuple'i birlestirme:
"""

tuple1 = ('a', 'b', 'c')
tuple2 = (1, 2, 3)

tuple3 = tuple1 + tuple2

print(tuple3)


#Multiply Tuples

"""
Bir tuple'in icerigini belirli bir sayida carpmak istiyorsaniz, '*' operatorunu
kullanabilirsiniz.

fruits tuple'ini 2 ile carpma:
"""

fruits = ("apple", "banana", "cherry")
mytuple = fruits * 2

print(mytuple)